
public class Variables {

	public static void main(String[] args) {
		byte edad;
		
		edad=35; // Declaración variable y comentario de una línea
		
		System.out.println(edad); /* la siguiente instrucción imprime en la consola (System.out) el 
		valor de la variable edad*/
		edad = 75;
		System.out.println(edad);
	}

}
